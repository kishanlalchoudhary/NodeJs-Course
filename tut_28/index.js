// Commands in Mongosh
// show dbs - it is used to list all dbs
// use <db_name> - to create a new db and also can be used to switch between dbs in mongosh
// new db is only visible when there is atleast one collection
// db.createCollection("collection_name") - it is used to create a collection with given collection name
// show collections - to view all collections
// db.<collection_name>.drop() - it is used to delete a collection from a particular database
// db - it is used to check in which database your mongosh is operated
// db.dropDatabase() - it is used to delete a database in which your mongosh is operating